import sys
import os
from logging.config import fileConfig

from sqlalchemy import engine_from_config
from sqlalchemy import pool

from alembic import context

# Allow imports from the backend package
sys.path.insert(0, os.path.dirname(os.path.dirname(__file__)))

# Import settings for DATABASE_URL
from app.config import settings

# Import all models so autogenerate can detect them
from app.database import Base
import app.models.user          # noqa: F401
import app.models.listing       # noqa: F401
import app.models.message       # noqa: F401
import app.models.request       # noqa: F401
import app.models.notification  # noqa: F401
import app.models.review        # noqa: F401
import app.models.transaction   # noqa: F401
import app.models.saved_listing  # noqa: F401
import app.models.announcement   # noqa: F401
import app.models.report         # noqa: F401
import app.models.admin_log      # noqa: F401
import app.models.system_setting # noqa: F401

# this is the Alembic Config object, which provides
# access to the values within the .ini file in use.
config = context.config

# Override sqlalchemy.url from .env (ignores whatever is in alembic.ini)
config.set_main_option("sqlalchemy.url", settings.database_url)

# Interpret the config file for Python logging.
if config.config_file_name is not None:
    fileConfig(config.config_file_name)

target_metadata = Base.metadata


def run_migrations_offline() -> None:
    url = config.get_main_option("sqlalchemy.url")
    context.configure(
        url=url,
        target_metadata=target_metadata,
        literal_binds=True,
        dialect_opts={"paramstyle": "named"},
    )

    with context.begin_transaction():
        context.run_migrations()


def run_migrations_online() -> None:
    connectable = engine_from_config(
        config.get_section(config.config_ini_section, {}),
        prefix="sqlalchemy.",
        poolclass=pool.NullPool,
    )

    with connectable.connect() as connection:
        context.configure(
            connection=connection,
            target_metadata=target_metadata,
        )

        with context.begin_transaction():
            context.run_migrations()


if context.is_offline_mode():
    run_migrations_offline()
else:
    run_migrations_online()
